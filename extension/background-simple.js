async function sendToBackend(url) {
  recentScans.set(url, Date.now());
  
  console.log('ğŸ“¤ Sending:', url);
  
  try {
    const response = await fetch(`${BACKEND_URL}/api/scan`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        url: url,
        timestamp: Date.now(),
        source: 'extension'
      })
    });
    
    if (response.ok) {
      const result = await response.json();
      console.log('âœ… Success:', result.risk);
    } else {
      console.error('âŒ Failed:', response.status);
    }
  } catch (error) {
    console.error('âŒ Error:', error.message);
  }
}

